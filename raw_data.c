static void renderBuffer(double x, double y, double z,
                            int scaling, double scale,
                            double vbo[][2], int ibo[][3], int length){
    int i;
    if(scaling){
        for(i=0;i<length;i++){
            vbo[i][0] *= scale;
            vbo[i][1] *= scale;
        }
    }
    glPushMatrix();
    glTranslatef(x,y,z);
    glDisable(GL_LIGHTING);
    glBegin(GL_TRIANGLES);
    glNormal3f(0.0,0.0,1.0);
    for(i=0;i<length;i++){
        glVertex3d(vbo[ibo[i][0]][0],vbo[ibo[i][0]][1],0.0);
        glVertex3d(vbo[ibo[i][1]][0],vbo[ibo[i][1]][1],0.0);
        glVertex3d(vbo[ibo[i][2]][0],vbo[ibo[i][2]][1],0.0);
    }
    glEnd();
    glEnable(GL_LIGHTING);
    glPopMatrix();
};

